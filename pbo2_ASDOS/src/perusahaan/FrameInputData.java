    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        AlamatADT almtLkp = new AlamatADT();
        Karyawan karyawan = new Karyawan();
        String NPP = this.isianNPP.getText();
        String nm = this.isianNama.getText();
        String almt1 = this.isianNamaJalan.getText();
        String almt2 = this.isianRTRWKec.getText();
        String almt3 = this.isianKota.getText();
        String jabatan = (String)this.pilihanJabatan.getSelectedItem();
        almtLkp.setNamaJalan(almt1);
        almtLkp.setRTRWKec(almt2);
        almtLkp.setKota(almt3);
        karyawan.setNPP(NPP);
        karyawan.setNama(nm);
        karyawan.setJabatan(jabatan);
        karyawan.setAlmtLkp(almtLkp);
        this.kary.add(karyawan);
        this.isianNPP.setText("");
        this.isianNama.setText("");
        this.isianNamaJalan.setText("");
        this.isianRTRWKec.setText("");
        this.isianKota.setText("");
        this.pilihanJabatan.setSelectedIndex(0);
        this.tampilButton.setEnabled(true);
    }//GEN-LAST:event_simpanButtonActionPerformed

