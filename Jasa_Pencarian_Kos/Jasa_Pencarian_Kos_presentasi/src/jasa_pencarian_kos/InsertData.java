    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        try {
            // TODO add your handling code here:
            if (jTextFieldNama.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong!");
                jTextFieldNama.requestFocus();;
            } else {
                if (jComboBoxTipeKamar.getSelectedIndex() == 0) {
                    JOptionPane.showMessageDialog(null, "Silahkan pilih tipe kamar !");
                } else {
                    if (jRadioButtonStatusSudah.isSelected() == false && jRadioButtonStatusBelum.isSelected() == false) {
                        JOptionPane.showMessageDialog(null, "Silahkan pilih status bayar!");
                    } else {
                        inputData();
                        tulis();
                        jTextFieldNama.setText("");
                        jComboBoxTipeKamar.setSelectedIndex(0);
                        jRadioButtonStatusBelum.setSelected(false);
                        jRadioButtonStatusSudah.setSelected(false);
                    }
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(InsertData.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(InsertData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

