    public void inputData() throws SQLException, IOException {
        conn = null;
        PreparedStatement ps = null;
        conn = kon.getConnection();
        String nama = jTextFieldNama.getText();
        String tipeKamar = (String) jComboBoxTipeKamar.getSelectedItem();
        String statusBayar = "BELUM";
        String tagihan = "";

        if (tipeKamar.equals("Tipe A")) {
            tagihan = "2000000";
        } else if (tipeKamar.equals("Tipe B")) {
            tagihan = "1500000";
        } else if (tipeKamar.equals("Tipe C")) {
            tagihan = "1000000";
        } else {
            JOptionPane.showMessageDialog(null, "Harap pilih tipe kamar terlebih dahulu !");
        }
        if (jRadioButtonStatusBelum.isSelected() == false) {
            statusBayar = "SUDAH";
        }
        String statusKamar = "TERISI";
        if (statusBayar == "SUDAH") {
        }
        try {
            ps = conn.prepareStatement("Insert into penghuni values(DEPT_SE.NEXTVAL,UPPER(?),?,?,?,?,?)");
            ps.setString(1, nama);
            ps.setString(2, tipeKamar);
            ps.setString(3, tagihan);
            ps.setString(4, statusKamar);
            ps.setString(5, statusBayar);
            if (statusBayar == "BELUM") {
                ps.setDate(6, null);
            } else {
                ps.setDate(6, Date.valueOf(waktu));

            }
            ps.executeUpdate();
            conn.commit();
            JOptionPane.showMessageDialog(null, "Data sudah ditambahkan!");

        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        } finally {
            try {
                ps.close();
                conn.close();
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }
        
}
    }

    public void tulis() throws IOException, SQLException {
        conn = kon.getConnection();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(" SELECT * FROM PENGHUNI ");
        FileWriter fileWriter = new FileWriter("Data Pembayaran.txt");
        fileWriter.write("DATA PEMBAYARAN BULAN " + waktu.getMonth());
        fileWriter.write("\n===============================================================");
        fileWriter.write("\nNAMA\t\tTIPE KAMAR\tTAGIHAN\t\tTANGGAL BAYAR ");
        fileWriter.write("\n===============================================================");
        while (rs.next()) {
            try {
                Date tam = rs.getDate(7);
                if (tam == null) {
                    fileWriter.write("\n" + rs.getString(2) + "\t\t" + rs.getString(3) + "\t\t" + rs.getString(4) + "\t\tBELUM BAYAR");
                } else {
                    fileWriter.write("\n" + rs.getString(2) + "\t\t" + rs.getString(3) + "\t\t" + rs.getString(4) + "\t\t" + rs.getDate(7));
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
        fileWriter.close();
        rs.close();
        st.close();
        conn.close();
    }

