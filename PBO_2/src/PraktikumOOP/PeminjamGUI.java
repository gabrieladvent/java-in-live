    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        peminjam = new ArrayList<>();
        try{
            id = idText.getText();
            nama = nameText.getText();
            alamat = alamtText.getText();
            dataKeterangan = statusID.getText();
            max = Integer.parseInt(maxPinjam.getText());
            boolean angka = angkaSaja(dataKeterangan);
            boolean huruf = angkaSaja(id);
            cekTeks(dataKeterangan, id, nama, alamat, maxPinjam, angka, huruf);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Max pinjam harus diisi dengan angka");
        }catch(Exception ex ){
            JOptionPane.showMessageDialog(null, "Input yang dimasukkan salah");
        }
        if (penanda == false) {
            Peminjam dPeminjam = new Peminjam();
            switch(StatusComb.getSelectedIndex()){
                case 0 -> { 
                    dPeminjam = new Dosen(dataKeterangan);
                    dPeminjam.setIdPeminjam(id);
                    dPeminjam.setNama(nama);
                    dPeminjam.setAlamat(alamat);
                    dPeminjam.setMaxPinjam(max);
                    peminjam.add(dPeminjam);
                }
                case 1 -> {
                    dPeminjam = new Mahasiswa(dataKeterangan);
                    dPeminjam.setNama(nama);
                    dPeminjam.setIdPeminjam(id);
                    dPeminjam.setAlamat(alamat);
                    dPeminjam.setMaxPinjam(max);
                    peminjam.add(dPeminjam); 
                }
                case 2 -> { 
                    dPeminjam = new Umum(dataKeterangan);
                    dPeminjam.setNama(nama);
                    dPeminjam.setIdPeminjam(id);
                    dPeminjam.setAlamat(alamat);
                    dPeminjam.setMaxPinjam(max);
                    peminjam.add(dPeminjam);
                }
                default -> {
                }
            }
        }
        tableData.setValueAt(idText.getText(), x, 0);
        tableData.setValueAt(nameText.getText(), x, 1);
        tableData.setValueAt(StatusComb.getSelectedItem(), x, 2);
        tableData.setValueAt(statusID.getText(), x, 3);
        x = x + 1;
        for (Peminjam peminjam1 : peminjam) {
                if(max > 0){
                    JOptionPane.showMessageDialog(null, "Status : " +StatusComb.getSelectedItem() + "\n Nama : " + nama + "\n Alamat : " + alamat + "\n Max Pinjam : " + max);
                    JOptionPane.showMessageDialog(null, "Data Berhasil ditambahkan");
                }
       }
    }//GEN-LAST:event_saveButtonActionPerformed

