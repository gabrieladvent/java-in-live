    public void cekTeks(String dataKet, String id, String nama, String alamat, JTextField max, boolean angka1, boolean angka2) throws Exception{
        if (angka1 == false || angka2 == false) {
            throw new ExceptionKosong("Kolom ID harus diisi dengan angka dan tidak boleh kosong");
        }else if(dataKet.equals("") || id.equals("") || nama.equals("") || alamat.equals("") || statusID.getText().equals("")){
            throw new ExceptionKosong("Silahkan input data");
        }else{
            penanda = false;
        }
    }
    public static boolean angkaSaja(String cek){
        boolean angka = false;
        boolean huruf = false;
        for (int i = 0; i < cek.length(); i++) {
            if (Character.isDigit(cek.charAt(i))) {
                angka = true;
            }else{
                huruf = true;
            }
        }
        return (huruf == false && angka == true);
    } 
    

