    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try{
            judul = judulTxt.getText();
            penerbit = penerbitTxt.getText();
            switch(koleksiComboBox.getSelectedIndex()){
                //Disk
                case 0 -> {
                    Disk disk1 = new Disk(format, ISSN);
                    format = atrKoleksiTxt.getText();
                    ISSN = ISSNtxt.getText();
                    setDisk(judul, penerbit, format, ISSN);
                } 
                //Majalah
                case 1 -> {
                    Majalah majalah1 = new Majalah(vol, seri, ISSN);
                    vol = Integer.parseInt(atrKoleksiTxt.getText());
                    ISSN = ISSNtxt.getText();
                    seri = Integer.parseInt(seriTxt.getText());
                    setMajalah(judul, penerbit, atrKoleksiTxt, seriTxt, ISSN);
                }
                //Buku
                case 2 -> { 
                    Buku buku1 = new Buku(hal, ISSN);
                    hal = Integer.parseInt(atrKoleksiTxt.getText());
                    ISSN = ISSNtxt.getText();
                    setBuku(judul, penerbit, atrKoleksiTxt, ISSN);
                }
            }
        }catch(NumberFormatException e){
            switch(koleksiComboBox.getSelectedIndex()){
                case 0 ->{
                    JOptionPane.showMessageDialog(null, "Input Format dan ISSN tidak boleh kosong!!!", "", JOptionPane.ERROR_MESSAGE);
                }
                case 1 ->{
                    JOptionPane.showMessageDialog(null, "Input Volume dan Seri tidak boleh kosong dan di isi dengan angka!!!", "", JOptionPane.ERROR_MESSAGE);
                }
                case 2 ->{
                    JOptionPane.showMessageDialog(null, "Input Halaman tidak boleh kosong dan  di isi dengan angka!!!", "", JOptionPane.ERROR_MESSAGE);
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Input yang anda Masukan salah!!",JOptionPane.ERROR_MESSAGE);
        }
        if (mark == false) {
            switch (koleksiComboBox.getSelectedIndex()) {
                case 0 -> {
                    //Disk
                    Disk koleksi = new Disk(format, ISSN);
                    format = atrKoleksiTxt.getText();
                    ISSN = ISSNtxt.getText();
                    koleksi.setJudul(judul);
                    koleksi.setFormat(format);
                    koleksi.setISBN(ISSN);
                    koleksi.setPenerbit(penerbit);
                    disk.add(koleksi);
                    JOptionPane.showMessageDialog(null, "Judul " + judul + "\nPenerbit " + penerbit + "\nISBN " + ISSN + "\nFormat : " + format);
                }
                case 1 -> {
                    //Majalah
                    Majalah koleksi = new Majalah(vol, seri, ISSN);
                    vol = Integer.parseInt(atrKoleksiTxt.getText());
                    ISSN = ISSNtxt.getText();
                    seri = Integer.parseInt(seriTxt.getText());
                    koleksi.setVolume(vol);
                    koleksi.setISSN(ISSN);
                    koleksi.setJudul(judul);
                    koleksi.setSeri(seri);
                    koleksi.setPenerbit(penerbit);
                    majalah.add(koleksi);
                    JOptionPane.showMessageDialog(null, "Judul " + judul + "\nPenerbit " + penerbit + "\nVolume : " + vol + "\nSeri : " + seri + "\nISSN : " + ISSN);
                }
                case 2 -> {
                    //Buku
                    Buku koleksi = new Buku(hal, ISSN);
                    hal = Integer.parseInt(atrKoleksiTxt.getText());
                    ISSN = ISSNtxt.getText();
                    koleksi.setISBN(ISSN);
                    koleksi.setHal(hal);
                    koleksi.setJudul(judul);
                    koleksi.setPenerbit(penerbit);
                    buku.add(koleksi);
                    JOptionPane.showMessageDialog(null, "Judul " + judul + "\nPenerbit " + penerbit + "\nISBN " + ISSN + "\nHalaman : " + hal);
                }
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

