    public void setMajalah(String judul, String penerbit, JTextField volume, JTextField seri, String ISSN) throws Exception {
        if (judul.equals("") || penerbit.equals("") || volume.getText().equals("") || seri.getText().equals("") || ISSN.equals("")) {
            throw new ExceptionKosong("Input tidak boleh kosong");
        } else if (AngkaOnly(ISSN) == false) {
            throw new ExceptionKosong("ISSN tidak dapat di isi dengan huruf. \n Hanya bisa di isi dengan angka!!!");
        } else {
            mark = false;
        }
    }
    public void setBuku(String judul, String penerbit, JTextField halaman, String ISSN) throws Exception {
        if (judul.equals("") || penerbit.equals("") || halaman.getText().equals("") || ISSN.equals("")) {
            throw new ExceptionKosong("Input tidak boleh kosong");
        } else if (AngkaOnly(ISSN) == false) {
            throw new ExceptionKosong("ISBN tidak dapat di isi dengan huruf. \n Hanya bisa di isi dengan angka!!!");
        } else {
            mark = false;
        }
    }
    public void setDisk(String judul, String penerbit, String format, String ISSN) throws Exception {
        if (judul.equals("") || penerbit.equals("") || format.equals("") || ISSN.equals("")) {
            throw new ExceptionKosong("Teks tidak boleh ada yang kosong!");
        } else if (!(format.equalsIgnoreCase("DVD")) || !(format.equalsIgnoreCase("MP4") || !(format.equalsIgnoreCase("MP3")) )) {
            JOptionPane.showMessageDialog(null, "Judul " + judul + "\nPenerbit " + penerbit + "\nISBN " + ISSN + "\nFormat : " + format);
            throw new ExceptionKosong("Format Disk yang dimasukan salah");
        } else if (AngkaOnly(ISSN) == false) {
            throw new ExceptionKosong("ISBN tidak dapat di isi dengan huruf. \n Hanya bisa di isi dengan angka!!!");
        } else {
            mark = false;
        }
    }

    public static boolean AngkaOnly(String cek) {
        boolean angka = false;
        boolean tulisan = false;
        for (int i = 0; i < cek.length(); i++) {
            if (Character.isDigit(cek.charAt(i))) {
                angka = true;
            } else {
                tulisan = true;
            }
        }
        return (tulisan == false && angka == true);
    }
