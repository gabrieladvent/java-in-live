    public void tampilData(){
        String[] kolom = {"Jenis", "Nomor", "Pelanggan", "Abonemen", "Operator", "Tahun", "Paket Bicara/Internet/Sli", "Tipe/IMEI/Fitur"};
        Object[][] objData = new Object[TL.size()][8];
        
        String jenis;
        String Atribut1;
        String Atribut2;
        int i = 0;
        for (Telepon n : TL){
            if (n instanceof pstn_205314096) {
                jenis = "Telepon Kabel";
                Atribut1 = ((pstn_205314096) n).getPaket_bicara();
                Atribut2 = ((pstn_205314096) n).getTipe();
            } else if (n instanceof gsm_205314096) {    
                jenis = "SIM";
                Atribut1 = ((gsm_205314096) n).getPaket_internet();
                Atribut2 =((gsm_205314096) n).getImei();
            } else {
                jenis = "Telepon Nirkabel";
                Atribut1 = ((satelit_205314096) n).getPaket_sli();
                Atribut2 = ((satelit_205314096) n).getFitur();
            }
            objData[i] = new String[]{jenis, n.getNomor(), n.getPelanggan(), String.valueOf(n.getAbonemen()), n.getOperator(), 
                String.valueOf(n.getTahun()), Atribut1, Atribut2};
        i++;
        }
        tabel = new DefaultTableModel(objData, kolom) {
            //membuat tabel tidak bisa diedit saat diklik
            @Override
            public boolean isCellEditable(int rowIndex, int colIndex) {
                return false;
            }
        };
        jTable1.setModel(tabel);
        column = jTable1.getColumnModel();
        column.getColumn(0).setPreferredWidth(150);
        column.getColumn(1).setPreferredWidth(150);
        column.getColumn(2).setPreferredWidth(150);
        column.getColumn(3).setPreferredWidth(150);
        column.getColumn(4).setPreferredWidth(150);
        column.getColumn(5).setPreferredWidth(150);
        column.getColumn(6).setPreferredWidth(250);
        column.getColumn(7).setPreferredWidth(250);
    }
    
