    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        String Nomor = nomor.getText();
        String Pelanggan = pelanggan.getText();
        int Abonemen = Integer.parseInt(abonemen.getText());
        String Operator = operator.getText();
        String atribut1 = TF6.getText();
        String atribut2 = TF7.getText();
        Telepon obj = null;
        
        //Pilihan untuk Mencari atau mengisi data//
        
           try{
               if (button.getText().equals("Isi Data")){
                int Tahun = Integer.parseInt(tahun.getText());
                LocalDate year = LocalDate.ofYearDay(Tahun, 1);
                    if (tlpnKabel.isSelected()){
                        obj = new pstn_205314096 (Nomor, Pelanggan, Abonemen, Operator, year, atribut1, atribut2);
                } else if (nirkabel.isSelected()){
                        obj = new satelit_205314096 (Nomor, Pelanggan, Abonemen, Operator, year, atribut1, atribut2);
                } else if (sim.isSelected()){
                        obj = new gsm_205314096 (Nomor, Pelanggan, Abonemen, Operator, year, atribut1, atribut2);
                }
                TeleponList.add(obj);
                JOptionPane.showMessageDialog(null,"Data Anda Berhasil Ditambahkan", "Sukses Ditambahkan", JOptionPane.INFORMATION_MESSAGE);
                } else if (button.getText().equals("Cari Data")){
            for (int i = 0; i < TeleponList.size(); i++) {
                Telepon check = TeleponList.get(i);
                if (check.getNomor().equals(Nomor)) {
                    JOptionPane.showMessageDialog(null, "Data yang Dicari Berada Di: " + (i + 1), "Sukses Ditemukan", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } else if (check.getPelanggan().equals(Pelanggan)) {
                    JOptionPane.showMessageDialog(null, "Data yang Dicari Berada Di: " + (i + 1), "Sukses Ditemukan", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                } 
                else{
                    JOptionPane.showMessageDialog(null, "Data Tidak Ditemukan\nSilahkan Cek Data Kembali", "Gagal Ditemukan", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
            }
        }
            } catch (Exception e){
               JOptionPane.showMessageDialog(this, "Silahkan Cek Kembali Data Anda", "Gagal Ditambahkan", JOptionPane.WARNING_MESSAGE);
                
            }
        
        clear();
        frTelepon back = new frTelepon();
        back.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_buttonActionPerformed

    private void nomorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nomorKeyReleased
        String Nomor = nomor.getText();
    if (tlpnKabel.isSelected() || nirkabel.isSelected()){
        if (Nomor.matches("^[+ 0-9]*$") && Nomor.length()==12){
            Fnomor.setSelected(true);
        } else{
            Fnomor.setSelected(false);
        }
    } else if (sim.isSelected()){
        if (Nomor.matches("^[+ 0-9]*$") && Nomor.length()==14){
            Fnomor.setSelected(true);
        } else{
            Fnomor.setSelected(false);
        }
    }
    }//GEN-LAST:event_nomorKeyReleased

    private void pelangganKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pelangganKeyReleased
        char Pelanggan = evt.getKeyChar();
        if(Character.isLetter(Pelanggan) || Character.isWhitespace(Pelanggan) || Character.isISOControl(Pelanggan)){
            pelanggan.setEditable(true);
            Fpelanggan.setSelected(true);
        } else{
            pelanggan.setEditable(false);
            Fpelanggan.setSelected(false);
        }
    }//GEN-LAST:event_pelangganKeyReleased

    private void abonemenKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_abonemenKeyReleased
        String Abonemen = abonemen.getText();
        if (Abonemen.matches("^[0-9]*$") && Abonemen.length() == 5 || Abonemen.length() == 6){
            Fabonemen.setSelected(true);
        } else{
            Fabonemen.setSelected(false);
        }
    }//GEN-LAST:event_abonemenKeyReleased

