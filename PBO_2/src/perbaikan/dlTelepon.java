    private void TF6KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TF6KeyReleased
        char atribut1 = evt.getKeyChar();
        if(Character.isLetter(atribut1) || Character.isWhitespace(atribut1) || Character.isISOControl(atribut1) || Character.isDigit(atribut1)){
            TF6.setEditable(true);
            Flabel6.setSelected(true);
        } else{
            TF6.setEditable(false);
            Flabel6.setSelected(false);
        }
    }//GEN-LAST:event_TF6KeyReleased

    private void TF7KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TF7KeyReleased
        if (tlpnKabel.isSelected() || nirkabel.isSelected()){
            char atribut2 = evt.getKeyChar();
        if(Character.isLetter(atribut2) || Character.isWhitespace(atribut2) || Character.isISOControl(atribut2)){
            TF7.setEditable(true);
            Flabel7.setSelected(true);
        } else{
            TF7.setEditable(false);
            Flabel7.setSelected(false);
        }
        } else if(sim.isSelected()){
            String atribut2 = TF7.getText();
            if (atribut2.matches("^[0-9]*$") && atribut2.length()==12){
            Flabel7.setSelected(true);
        } else{
            Flabel7.setSelected(false);
        }
        }  
    }//GEN-LAST:event_TF7KeyReleased

