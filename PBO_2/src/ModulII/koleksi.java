    private void cmbJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbJenisActionPerformed
    String kodeks;
        kodeks = String.valueOf(cmbJenis.getSelectedItem());   
         if (kodeks.equals("Majalah")) {
             AddMajalah mjlh = new AddMajalah();
             mjlh.setVisible(true);
             this.dispose();
        } else if (kodeks.equals("Buku")){
            AddBuku buku = new AddBuku();
            buku.setVisible(true);
            this.dispose();
        } else if (kodeks.equals("Disk")){
            AddDisk disk = new AddDisk();
            disk.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_cmbJenisActionPerformed

