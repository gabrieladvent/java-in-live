    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {
        int selectedRow = jTable1.getSelectedRow();
        txtjudul.setText(tabelModel.getValueAt(selectedRow, 0).toString());
        txtpenulis.setText(tabelModel.getValueAt(selectedRow, 1).toString());
        txtpenerbit.setText(tabelModel.getValueAt(selectedRow, 2).toString());
        txtisbn.setText(tabelModel.getValueAt(selectedRow, 3).toString());
    }

