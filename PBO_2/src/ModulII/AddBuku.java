    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nama = txtjudul.getText();
            String penulis = txtpenulis.getText();
            String penerbit = txtpenerbit.getText();
            int isbn = Integer.parseInt(txtisbn.getText());
         JOptionPane.showMessageDialog(this, "Judul Buku: " + txtjudul.getText()
                    + "\nPenulis: " + penulis
                    + "\nPenerbit: " + penerbit
                    + "\nISBN: " + isbn
                    + "\nBerhasil Ditambahkan");
            if (txtjudul.getText().equals("")) {
                txtjudul.setText("-");
            }
            if (txtpenulis.getText().equals("")) {
                txtpenulis.setText("-");
            }
            if (txtpenerbit.getText().equals("")) {
                txtpenerbit.setText("-");
            }
            if (txtisbn.getText().equals("")) {
                txtisbn.setText("-");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "'" + txtisbn.getText() + "' Invalid\n"
                    + "Silahkan Masukan Angka dengan Benar");
            txtisbn.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            txtisbn.setText("");
        }
        isiData(txtjudul.getText(), txtpenulis.getText(), txtpenerbit.getText(), Integer.parseInt(txtisbn.getText()));
        tampildata();
        clearInput();
    }//GEN-LAST:event_jButton1ActionPerformed

